package codeguide;

/**
 * The class is created getters and setters method of Student
 * 
 * @author Pittayut Sothanakul
 *
 */
public class Encap {

	private String name;
	private String idNum;
	private int age;

	/*
	 * get the name of student.
	 */
	public String getName() {
		return name;
	}

	/*
	 * get the id number of student.
	 */
	public String getIdNum() {
		return idNum;
	}

	/*
	 * get age of student.
	 */
	public int getAge() {
		return age;
	}

	/*
	 * set the name of student.
	 */
	public void setName(String name) {
		this.name = name;
	}

	/*
	 * set the id number of student.
	 */
	public void setIdNum(String idNum) {
		this.idNum = idNum;
	}

	/*
	 * set age of student.
	 */
	public void setAge(int age) {
		this.age = age;
	}

}
